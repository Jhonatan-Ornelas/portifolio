select.onchange = sortingValue;
function sortingValue(value) {
    if (value == 'Default') {
        while (field.firstChild) {
            field.removeChild(field.firstChild);
        }
        field.append(...ar);
    }
    if (value === 'LowToHigh') {
        sortElem(field, div, true, 'price');
    }
    if (value === 'HighToLow') {
        sortElem(field, div, false, 'price');
    }
    if (value === 'AToZ') {
        sortElem(field, div, true, 'name');
    }
    if (value === 'ZToA') {
        sortElem(field, div, false, 'name');
    }
}

function sortElem(field, div, asc, sortBy) {
    let dm, sortDiv;
    dm = asc ? 1 : -1;
    sortDiv = div.sort((a, b) => {
        let ax, bx;
        if (sortBy === 'price') {
            ax = priceToNumber(a.getAttribute('data-price'));
            bx = priceToNumber(b.getAttribute('data-price'));
        } else if (sortBy === 'name') {
            ax = a.querySelector('.item-decricao').textContent;
            bx = b.querySelector('.item-decricao').textContent;
        }

        return ax > bx ? 1 * dm : -1 * dm;
    });
    while (field.firstChild) {
        field.removeChild(field.firstChild);
    }
    field.append(...sortDiv);
}

